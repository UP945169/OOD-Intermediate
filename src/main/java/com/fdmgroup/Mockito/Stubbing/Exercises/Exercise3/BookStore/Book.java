package com.fdmgroup.Mockito.Stubbing.Exercises.Exercise3.BookStore;

public class Book {
    private double price;

    private String ISBN;

    public Book() {
        this.price = price;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }
}
